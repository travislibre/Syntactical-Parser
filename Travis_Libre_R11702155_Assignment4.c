/* lex - a simple lexical analyzer for arithmetic expressions */
int lex() {
    lexLen = 0;
    getNonBlank();

    switch (charClass) {
        /* Parse identifiers */
        case LETTER:
            addChar();
            while (charClass == LETTER || charClass == DIGIT) {
                addChar();
            }
            // Added strcmp statements to find keywords during IDENT identifier loop
            if (strcmp(lexeme, "read") == 0) {
                nextToken = KEY_READ;
            } else if (strcmp(lexeme, "print") == 0) {
                nextToken = KEY_PRINT;
            } else if (strcmp(lexeme, "while") == 0) {
                nextToken = KEY_WHILE;
            } else if (strcmp(lexeme, "do") == 0) {
                nextToken = KEY_DO;
            } else {
                nextToken = IDENT;
            }
            break;

            /* Parse integer literals */
        case DIGIT:
            addChar();
            while (charClass == DIGIT) {
                addChar();
            }
            nextToken = INT_LIT;
            break;

            /* Parentheses and operators */
        case UNKNOWN:
            lookup(nextChar);
            break;

            /* EOF */
        case EOF:
            nextToken = EOF;
            lexeme[0] = 'E';
            lexeme[1] = 'O';
            lexeme[2] = 'F';
            lexeme[3] = 0;
            break;
    } /* End of switch */
    //if (nextToken != EOF) // Answer does not show EOF tokens
    //    printAnswer();
    return nextToken;
} /* End of function lex */
/* lookup - a function to lookup operators and parentheses and return the
 * token
 * !!!
 * Added additional functionality for the multi-character lexemes
 * */
static int lookup(char ch) {
    switch (ch) {
        case '(':
            addChar();
            nextToken = LEFT_PAREN;
            break;
        case ')':
            addChar();
            nextToken = RIGHT_PAREN;
            break;
        case '+':
            addChar();
            nextToken = ADD_OP;
            break;
        case '-':
            addChar();
            nextToken = SUB_OP;
            break;
        case '*':
            addChar();
            nextToken = MULT_OP;
            break;
        case '/':
            addChar();
            nextToken = DIV_OP;
            break;
        case '<':
            addChar();
            if (nextChar == '=') { // Checks if lookahead character is the required next character for the lexeme
                // If so, add to lexeme
                addChar();
                nextToken = LEQUAL_OP;
                break;
            } else if (nextChar == '>') {
                addChar();
                nextToken = NEQUAL_OP;
                break;
            } else {
                //Else, initial character is the lexeme
                nextToken = LESSER_OP;
                break;
            }
        case '>':
            addChar();
            if (nextChar == '=') {
                addChar();
                nextToken = GEQUAL_OP;
                break;
            } else {
                nextToken = GREATER_OP;
                break;
            }
        case '=':
            addChar();
            if (nextChar == '=') {
                addChar();
                nextToken = EQUAL_OP;
                break;
            } else {
                nextToken = ASSIGN_OP;
                break;
            }
        case '{':
            addChar();
            nextToken = LEFT_CBRACE;
            break;
        case '}':
            addChar();
            nextToken = RIGHT_CBRACE;
            break;
        case ';':
            addChar();
            nextToken = SEMICOLON;
            break;
        case '%':
            addChar();
            nextToken = MOD_OP;
            break;
        default:
            addChar();
            nextToken = UNKNOWN;
            break;
    }
    return nextToken;
}

