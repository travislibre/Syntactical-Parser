#define MOD_OP 34
#define LEFT_CBRACE 35
#define RIGHT_CBRACE 36

/* Keyword codes */
#define KEY_READ 37
#define KEY_PRINT 38
#define KEY_WHILE 39
#define KEY_DO 40

int globalError = 0;
int globalInsideDoWhile = 0;

int lex(); // Lexeme function

/* Global Variable */
int nextToken; // Represents look-ahead

/* Local Variables */
static int charClass;
static char lexeme[100];
