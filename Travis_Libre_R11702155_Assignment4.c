void factor();

void formCheck() {
    if (nextToken == LEFT_PAREN) {
        lex();
        if (nextToken == IDENT) {
            lex();
            if (nextToken == RIGHT_PAREN) {
                lex();
            } else
                error();
        } else
            error();
    } else
        error();
}

void statement() {
    //printf("Enter <statement>\n");
    if (nextToken == IDENT) {
        lex();
        if (nextToken == ASSIGN_OP) {
            lex();
            expr();
        } else {
            error();
        }
    } else if (nextToken == KEY_READ) {
        lex();
        formCheck();
    } else if (nextToken == KEY_PRINT) {
        lex();
        formCheck();
    } else if (nextToken == KEY_DO) {
        lex();
        if (nextToken == LEFT_CBRACE) {
            lex();
            globalInsideDoWhile++;
            statement();
            globalInsideDoWhile--;
            if (nextToken == RIGHT_CBRACE) {
                lex();
                if (nextToken == KEY_WHILE) {
                    lex();
                    condition();
                } else error();
            } else error();
        } else error();
    } else {
        //printf("Statement error");
        error();
    }
    if (nextToken == SEMICOLON) {
        lex();
        statement();
    }
    //printf("Exit <stmt>\n");
}

void condition() {
    //printf("Enter <cond>");
    expr();
    if (nextToken == LEQUAL_OP || nextToken == GEQUAL_OP || nextToken == EQUAL_OP ||
        nextToken == NEQUAL_OP || nextToken == GREATER_OP || nextToken == LESSER_OP) {
        lex();
        expr();
    } else error();
    //printf("Exit <cond>");
}

/* expr
 * Parses strings in the language generated by the rule:
 * <expr> -> <term> {(+ | -) <term>}
 */
void expr() {
    //printf("Enter <expr>\n");

    /* Parse the first term */
    term();

    /* As long as the next token is + or -, get
    the next token and parse the next term */
    while (nextToken == ADD_OP || nextToken == SUB_OP) {
        lex();
        term();
    }


    //printf("Exit <expr>\n");
} /* End of function expr */

/* term
 * Parses strings in the language generated by the rule:
 * <term> -> <factor> {(* | /) <factor>)
 */
void term() {
    //printf("Enter <term>\n");
    /* Parse the first factor */
    factor();
    /* As long as the next token is * or /, get the
    next token and parse the next factor */
    while (nextToken == MULT_OP || nextToken == DIV_OP || nextToken == MOD_OP) {
        lex();
        factor();
    }
    //printf("Exit <term>\n");
} /* End of function term */

/* factor
 * Parses strings in the language generated by the rule:
 * <factor> -> id | int_constant | ( <expr )
 * */
void factor() {
    //printf("Enter <factor>\n");
    /* Determine which RHS */
    if (nextToken == IDENT || nextToken == INT_LIT) {
        lex(); /* Get the next token */
    } else {
        /* If the RHS is (<expr>), call lex to pass over the
        left parenthesis, call expr, and check for the right
        parenthesis */
        if (nextToken == LEFT_PAREN) {
            lex();
            expr();

            if (nextToken == RIGHT_PAREN) {
                lex();
            } else {
                error();
            }
        } /* End of if (nextToken == ... */
            /* It was not an id, an integer literal, or a left parenthesis */
        else {
            error();
        }
    } /* End of else */
    //printf("Exit <factor>\n");;
} /* End of function factor */

static void error() {
    if (globalError==0){
        printf("Error Encounter: The next lexeme was ");
        printAnswer();
    }
    globalError++;
}

